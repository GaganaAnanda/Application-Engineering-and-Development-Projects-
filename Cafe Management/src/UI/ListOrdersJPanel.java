    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        // TODO add your handling code here:
    int selectedRow = tblListOrders.getSelectedRow();
    if (selectedRow < 0) {
        JOptionPane.showMessageDialog(this, "Please select a row from table first", "Warning", JOptionPane.WARNING_MESSAGE);
        return;
    }
    
    // Get customer ID from selected row and find the customer
    long customerID = (Long) tblListOrders.getValueAt(selectedRow, 0);
    
    for (Customer customer : customers) {
        if (customer.getCustomerID() == customerID) {
            selectedCustomer = customer;
            break;
        }
    }
    
    if (selectedCustomer != null) {
        // Populate form fields
        fieldCusID.setText(String.valueOf(selectedCustomer.getCustomerID()));
        fieldOrderID.setText(String.valueOf(selectedCustomer.getOrder().getOrderID()));
        fieldProduct.setText(selectedCustomer.getOrder().getProduct().getName());
        fieldPrice.setText(String.valueOf(selectedCustomer.getOrder().getProduct().getPrice()));
    }

    }//GEN-LAST:event_btnViewActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblListOrders.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(this, "Please select a row from table first", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }

        // Get customer ID from selected row
        long customerID = (Long) tblListOrders.getValueAt(selectedRow, 0);

        // Find and remove the customer from the list
        Customer customerToRemove = null;
        for (Customer customer : customers) {
            if (customer.getCustomerID() == customerID) {
                customerToRemove = customer;
                break;
            }
        }

        if (customerToRemove != null) {
            customers.remove(customerToRemove);
        }
       
    }//GEN-LAST:event_btnDeleteActionPerformed


