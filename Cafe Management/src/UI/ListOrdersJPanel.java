    private void populateOrdersTable() {
        DefaultTableModel model = (DefaultTableModel) tblListOrders.getModel();
        model.setRowCount(0);

        for (Customer customer : customers) {
            Object[] row = new Object[4];
            row[0] = customer.getCustomerID();
            row[1] = customer.getOrder().getOrderID();
            row[2] = customer.getOrder().getProduct().getName(); // Product name instead of object
            row[3] = customer.getOrder().getProduct().getPrice();

            model.addRow(row);
        }
}

